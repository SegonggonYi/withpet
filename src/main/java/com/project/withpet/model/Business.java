package com.project.withpet.model;

public class Business {

    private int bus_no;
    private String bus_name;
    private String bus_addr;
    private String bus_tel;
    private String bus_sort;
    private String bus_id;
    private int bus_accept;

    public int getBus_no() {
        return bus_no;
    }

    public void setBus_no(int bus_no) {
        this.bus_no = bus_no;
    }

    public String getBus_name() {
        return bus_name;
    }

    public void setBus_name(String bus_name) {
        this.bus_name = bus_name;
    }

    public String getBus_addr() {
        return bus_addr;
    }

    public void setBus_addr(String bus_addr) {
        this.bus_addr = bus_addr;
    }

    public String getBus_tel() {
        return bus_tel;
    }

    public void setBus_tel(String bus_tel) {
        this.bus_tel = bus_tel;
    }

    public String getBus_sort() {
        return bus_sort;
    }

    public void setBus_sort(String bus_sort) {
        this.bus_sort = bus_sort;
    }

    public String getBus_id() {
        return bus_id;
    }

    public void setBus_id(String bus_id) {
        this.bus_id = bus_id;
    }

    public int getBus_accept() {
        return bus_accept;
    }

    public void setBus_accept(int bus_accept) {
        this.bus_accept = bus_accept;
    }
}
