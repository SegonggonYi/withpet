package com.project.withpet.model;

public class Pet {
    private int pet_no;
    private String pet_sort;
    private String pet_kind;
    private String pet_name;
    private String pet_gender;
    private String pet_birth;
    private String pet_photo;
    private String pet_info;
    private String pet_parent;

    public int getPet_no() {
        return pet_no;
    }

    public void setPet_no(int pet_no) {
        this.pet_no = pet_no;
    }

    public String getPet_sort() {
        return pet_sort;
    }

    public void setPet_sort(String pet_sort) {
        this.pet_sort = pet_sort;
    }

    public String getPet_kind() {
        return pet_kind;
    }

    public void setPet_kind(String pet_kind) {
        this.pet_kind = pet_kind;
    }

    public String getPet_name() {
        return pet_name;
    }

    public void setPet_name(String pet_name) {
        this.pet_name = pet_name;
    }

    public String getPet_gender() {
        return pet_gender;
    }

    public void setPet_gender(String pet_gender) {
        this.pet_gender = pet_gender;
    }

    public String getPet_birth() {
        return pet_birth;
    }

    public void setPet_birth(String pet_birth) {
        this.pet_birth = pet_birth;
    }

    public String getPet_photo() {
        return pet_photo;
    }

    public void setPet_photo(String pet_photo) {
        this.pet_photo = pet_photo;
    }

    public String getPet_info() {
        return pet_info;
    }

    public void setPet_info(String pet_info) {
        this.pet_info = pet_info;
    }

    public String getPet_parent() {
        return pet_parent;
    }

    public void setPet_parent(String pet_parent) {
        this.pet_parent = pet_parent;
    }
}
